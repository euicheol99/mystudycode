    BOOKNUMBER INT NOT NULL,  -- 책 번호 (외래키로 사용)
    MEMBERNO INT NOT NULL,  -- 회원 번호 (외래키로 사용)
    BOOKNAME VARCHAR(50) NOT NULL,  -- 책 제목
    BOOKAUTHOR VARCHAR(50) NOT NULL,  -- 저자
