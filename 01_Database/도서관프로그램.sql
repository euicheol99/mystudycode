    FOREIGN KEY (BOOKNUMBER) REFERENCES BOOK(BOOKNUMBER),  -- 책 번호 외래키
    FOREIGN KEY (MEMBERNO) REFERENCES MEMBER(MEMBERNO)  -- 회원 번호 외래키
);
